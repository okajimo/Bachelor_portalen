        <h3>Dokumentasjon</h3>

            På datalinjen er det fastsatt en dokumentasjonsstandard for bachelorprosjekter.
            Denne standarden skal følges. Dokumentasjonsstandarden kan ses under dokumentasjon i menyen til venstre.<br/><br/>
            Dokumentasjonsstandarden forteller hvordan de ulike rapportene skal utformes og det gis eksempler
            fra tidligere bachelorprosjekter. Det kan være tilfeller der datalinjens dokumentasjonsstandard kan fravikes.
            Hvis f.eks. oppdragsgiver har bestemte krav til hvordan produktdokumentasjonen skal lages,
            vil det være naturlig å følge oppdragsiverens standard.

        <h3>Intern veileder</h3>
        
            Hver prosjektgruppe blir tildelt en intern veileder. Dette vil skje etter at alle prosjektskissene
            har blitt sendt inn. En oversikt over dette blir offentliggjort på prosjektoversikten.
            Hvordan dere skal samarbeide med intern veileder står det mer om på veiledersiden.
        
        <h3>Avtale med oppdragsgiveren</h3>

            Det skal normalt inngås en avtale mellom oppdragsgiver og høgskolen.
            Den skal beskrive partenes plikter og rettigheter.
            Det er viktig at avtalen diskuteres med og underskrives av en person på ansvarlig nivå hos oppdragsgiver.
            D.v.s. det må være en person som har rett til å sette i gang prosjekter og rett til å tildele ressurser
            og personer til veiledning og hjelp. Husk at det er en avtale mellom høgskolen og oppdragsgiver,
            og ikke mellom studentgruppen og oppdragsgiver. Under prosjektarbeidet er dere studenter
            og underlagt høgskolen og høgskolens regler. Det er laget en standardavtale (english version)
            til dette formålet.<br/><br/>
            Helt tidlig i denne fasen skal høgskolens interne veileder sammen med studentgruppen
            ha et møte hos oppdragsgiver. Hensikten er å bli kjent, diskutere samarbeidet og
            vurdere om standardavtalen trenger et tillegg. Prosjektskissen skal være et vedlegg
            til samarbeidsavtalen. Avtalen skal undertegnes av studielederen på vegne av høgskolen.
            Hvis oppdragsgiver ønsker at avtalen skal være undertegnet før det er tildelt intern veileder
            til prosjektet, må det tas opp med kontaktpersonen for bachelorprosjekter.

        <h3>Talskvinne/mann for prosjektgruppen</h3>

            Høgskolen vil forsøke å legge forholdene best mulig til rette for det praktiske arbeidet med prosjektet.
            Vi vil så langt det er mulig la gruppene få disponere faste terminaler til prosjektarbeidet.
            Tilleggsutstyr og spesiell programvare kan hvis budsjettsituasjonen tillater det og hvis det er
            ting som høgskolen ellers vil ha generell nytte av, kjøpes inn i forbindelse med prosjektet.
            (Normalt skal oppdragsgiver stille med utstyr og programvare som går ut over det vi allerede har
            til studentbruk.)<br/><br/>
            Prosjektgruppen må velge en talskvinne eller talsmann. Det er denne personen som vil være kontaktperson
            overfor laboratoriepersonalet, datastudiet og høgskolen når det gjelder de spørsmålene som er nevnt over.
            Dette er sterkt ønsket bl.a. av laboratoriepersonalet slik at de har færrest mulig antall personer å
            forholde seg til. Det kan bli aktuelt at alle talskvinnene/mennene tidlig i denne fasen blir innkalt
            til et møte sammen med bachelorprosjektenes kontaktperson og laboratoriepersonalet for å diskutere praktiske
            spørsmål i forbindelse med prosjektarbeidet.

        <h3>Forprosjektet</h3>

            Forprosjektet starter så snart prosjektet er godkjent som bachelorprosjekt og intern veileder er tildelt,
            og senest første studieuke i januar. Det skal føres en prosjektdagbok i hele prosjektperioden.
            Prosjektperioden startet i det øyeblikket dere dannet prosjektgruppen og dere kom frem til hvilket
            prosjekt dere skulle satse på. <br/> <br/>
            I forprosjektet skal problemområdet ytterligere defineres. En skal bestemme så nøyaktig og presist
            som mulig hva problemområdet består av. Et viktig poeng er om problemet kan løses innenfor de rammene
            som er aktuelle, d.v.s. tids- og arbeidsmessige rammer og de gitte teknologiske rammene. Hvis ikke må problemet
            avgrenses og/eller omdefineres. Maskinplattform og utviklingsmiljø må bestemmes og arbeidsplan for resten
            av prosjektperioden utarbeides. Det er om og gjøre å få flest mulig brikker på plass før spillet kan begynne
            for alvor.

        <h3>Forprosjektrapport</h3>

            Forprosjektet avsluttes med at det lages en forprosjektrapport sammen med en arbeids- og fremdriftsplan
            for resten av prosjektet. Retningslinjer for hvordan disse skal lages vil dere finne i dokumentasjonsstandarden.
            Fristen for innlevering finner dere i huskelisten. Rapporten legges (en lenke) under den hjemmesiden dere har
            oppgitt i prosjektoversikten.
